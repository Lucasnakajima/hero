public class Arena {
    private int height;
    private int width;

    Arena(int h, int w){
        this.height = h;
        this.width = w;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }


}
